#include <unistd.h>

void	aux(int n, char c)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = n * (-1);
	}
	if (n < 10)
	{
		c = n + 48;
		write(1, &c, 1);
		return ;
	}
	else
	{
		c = n % 10 + 48;
		aux(n / 10, 'a');
		write(1, &c, 1);
	}
}

void	ft_putnbr(int nb)
{
	aux(nb, 'a');
}
