#include <unistd.h>

void	ft_is_negative(int n)
{
	char	c1;

	if (n >= 0)
	{
		c1 = 'P';
		write(1, &c1, 1);
	}
	else
	{
		c1 = 'N';
		write(1, &c1, 1);
	}
}
